using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace FANS.classes
{
    class StatusEventArgs : EventArgs
    {
            public string Status;
            public int Min,Max,Current;
            public StatusEventArgs(string status,int min=0,int max=0,int current=0)
                : base()
            {
                Status=status;
                Min=min;
                Max=max;
                Current=current;
            }


        }
    }


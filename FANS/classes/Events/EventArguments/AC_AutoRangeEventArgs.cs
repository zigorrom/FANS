using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace FANS.classes
{
    class AC_AutoRangeEventArgs:EventArgs
    {
        public double Range;
        public bool isBipolar;
        public int NumberOfChannel;
        public AC_AutoRangeEventArgs(double range, bool polarity , int number)
            : base()
        {
            Range = range;
            isBipolar = polarity;
            NumberOfChannel = number;
        }
    }
        
}
